# calcule sua média escolar do ano

num1 = int(input("Digite a nota do primeiro bimestre: "))
num2 = int(input("Digite a nota do segundo bimestre: "))
num3 = int(input("Digite a nota do terceiro bimestre: "))
num4 = int(input("Digite a nota do quarto bimestre: "))
media = (num1 + num2 + num3 + num4) / 4
print(f"Sua nota final é {media}")